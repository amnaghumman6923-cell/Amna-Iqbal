#include <iostream>
using namespace std;

int main() 
{
    int num1 = 10;  
    int num2 = 20;   
    double average;  
    
    average = (num1 + num2) / 2.0;  // Use 2.0 to get decimal result

    cout << "The average of " << num1 << " and " << num2 << " is " << average << endl;

    return 0;
}
